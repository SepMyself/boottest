package com.example.boottest.util;

public class TokenInfo {
    private String id;
    private String flag;

    public TokenInfo(){}

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
}
